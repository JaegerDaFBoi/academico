    {
        $nota = new Note();

        $nota->nota = $request->nota;
        $nota->student_id = $request->estudiante;
        $nota->course_id = $request->materia;
        

        $nota->save();
        session()->flash("flash.banner", "Nota creada de manera satisfactoria");

        return Redirect::route('notas.index');
    }

